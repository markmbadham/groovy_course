class Numbers {
    static void main(args) {
        def age = 10
        def bytes = 050
        def len = 4.5
        def nano = 3E-9
        println(age)
        println(age.getClass())
        println(bytes.getClass())
        println(len.getClass())
        println(nano.getClass())
        println(nano * 5)
    }
}